public class Coordinate {
    /**
     * x轴坐标值
     */
    public int x;
    /**
     * y轴坐标值
     */
    public int y;
    /**
     * z轴坐标值
     */
    public int z;

    public Coordinate(int x, int y, int z) {
        this.x = x;
        this.y = y;
        this.z = z;
    }
}
